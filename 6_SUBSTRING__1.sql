SELECT
	contactname,
	SUBSTRING(contactname, 0, CHARINDEX(N' ', contactname)) AS lastname
FROM Customers;

---------------------------------------------------------------------
-----------------------------------------------------------------



-- using FORMAT
SELECT
	CustomerID,
	FORMAT(CustomerID, N'\C00000') AS custnewid
FROM Customers;

---------------------------------------------------------------------

---------------------------------------------------------------------

SELECT
	contactname,
	LEN(contactname)  AS number
FROM Customers
ORDER BY number DESC;

